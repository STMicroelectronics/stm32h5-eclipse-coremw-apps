/**************************************************************************/
/*                                                                        */
/*  FUNCTION                                               RELEASE        */
/*                                                                        */
/*    _ux_os_utility_thread_identify                      PORTABLE C      */
/*                                                           6.4.1        */
/*  AUTHOR                                                                */
/*                                                                        */
/*    STMicroelectronics                                                  */
/*                                                                        */
/*  DESCRIPTION                                                           */
/*                                                                        */
/*    This function return a pointer to the calling thread.               */
/*                                                                        */
/*  INPUT                                                                 */
/*                                                                        */
/*    None                                                                */
/*                                                                        */
/*  OUTPUT                                                                */
/*                                                                        */
/*    Completion Status                                                   */
/*                                                                        */
/*  CALLS                                                                 */
/*                                                                        */
/*    xTaskGetSchedulerState                  FreeRTOS get thread state   */
/*    xTaskGetCurrentTaskHandle               FreeRTOS get current thread */
/*                                                                        */
/*  CALLED BY                                                             */
/*                                                                        */
/*    USBX Components                                                     */
/*                                                                        */
/**************************************************************************/
UX_THREAD *_ux_os_utility_thread_identify(VOID)
{
  TaskHandle_t current_task_handle;
  UX_THREAD *thread_ptr;

  if (xTaskGetSchedulerState() != pdTRUE)
  {
    /* Return a pointer to the handle of the current task. */
    current_task_handle = xTaskGetCurrentTaskHandle();
    thread_ptr = pvTaskGetThreadLocalStoragePointer(current_task_handle, UX_THREAD_POINTER_INDEX);

    return thread_ptr;
  }
  else
  {
    /* Error trap. */
    _ux_system_error_handler(UX_SYSTEM_LEVEL_THREAD, UX_SYSTEM_CONTEXT_UTILITY, UX_THREAD_ERROR);
    return UX_NULL;
  }
}
